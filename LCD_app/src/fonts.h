/*

Get ZLCD_fonts of any type here in ttf format.
See https://www.dafont.com/ for ttf files

then pick the desired ZLCD_font size and convert to C 
code using the LVGL converter:

https://lvgl.io/tools/fontconverter

Set the desired ZLCD_font size in the tool and pick the range (32-127)
for readable characters only

Paste in only the two arrays here and name them as desired.
Since the ZLCD_font size is not actually contained in the arrays,
it makes sense to name the arrays by ZLCD_font name and size.
Every ZLCD_font will have both the bitmap for each character (uint_t*) and 
the array of metadata structs (glyph_dsc_t*). The names should match
for each pair of arrays to make working with ZLCD_fonts easy and simple.
the second array needs to be an array of type glyph_dsc.

*/

